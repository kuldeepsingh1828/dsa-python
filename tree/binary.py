if __name__ == "__main__":
    bst = BinarySearchTree()
    bst.insert(50)
    bst.insert(30)
    bst.insert(20)
    bst.insert(40)
    bst.insert(70)
    bst.insert(60)
    bst.insert(80)

    print("Inorder traversal of the BST:")
    bst.inorder_traversal()  # Output: 20 30 40 50 60 70 80
    print("\n")

    print("Deleting 20:")
    bst.delete(20)
    bst.inorder_traversal()  # Output: 30 40 50 60 70 80
